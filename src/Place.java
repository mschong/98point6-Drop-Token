

public class Place {
	private int x;
	private int y;
	private int player;
	
	public Place(int x, int y) {
		this.x = x;
		this.y = y;
		this.player = 0;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getPlayer() {
		return player;
	}

	public void setPlayer(int player) {
		this.player = player;
	}
	
	
	
}
